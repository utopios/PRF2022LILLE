/**
 * LES OPERATEURS DE COMPARAISON
 */

/*
    Les opérateurs de comparaison

    == signifie est égal à // Permet de vérifier que deux valeurs sont identiques

    === signifie strictement égal à // Compare la valeur et le type de deux variables

    != signifie est différent de // Permet de vérifier que la valeur de deux variables sont différentes

    !== signifie strictement différent de // Permet de vérifier si le type OU la valeur sont différentes
*/

// EXEMPLES
console.log(1 == 1); // vrai
console.log("1" == 1); // vrai
console.log(1 === 1); // vrai
console.log("1" === 1); // faux
console.log("1" != 1 ); // faux
console.log("3" !== 1 ); // vrai
console.log(3 !== 3); // faux
console.log(4 !== 3); // vrai



